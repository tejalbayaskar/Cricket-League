    <p>
      
The washout in Trinidad was sad in many respects none being bigger than that it denied the Indian team the chance to win the Test and stay on top of the Test rankings. Of course, there is no guarantee that they would have won the Test but the manner in which they had won the third Test match when a draw looked the most likely result at lunch time on Day IV showed the determination of Virat Kohli’s team to take that extra step and go for a win.<br>

With 14 sessions out of the 15 not played despite bright sunshine was even more frustrating for the young Indian team who kept coming to the ground hoping for some action but had to leave soon since the umpires didn’t think it fit for play. <br>
There is a heavy crowded season ahead for the Indians so it’s understandable that no player wanted to risk an injury and luckily there were only a handful of spectators at the ground so there was no protest at the non-action.<br>

The ICC has set up an inquiry at the washout in Trinidad and Durban and while nobody can fight nature, the one thing is for sure. Test cricket can ill afford such situations where despite the sun shining the play is not happening.<br>

India not just won the Test series but it also won hearts as the players were seen to mingle with their fans and sign autographs and pose for pictures. The ogre of match fixing has made Indian players wary of anybody who approaches them even for an autograph and so they have in the past  given the impression of being aloof and unapproachable.<br>
<br>
Luckily this bunch of youngsters is the most happy delightful group that you could find.<br>

They are confident, happy in their own skins and so are accessible to meeting new people. Sure the anti-corruption guys will be keeping an eye on those around and the players themselves are now wise to the blandishments and all and know when to step back.<br>

Unfortunately the modern technology of phone cameras has made it extremely tough for the players. Any function that they go to especially where it involves other Indians is just a melee for everybody to just take out their phones and start clicking.<br>

Naturally the players shrink a bit and then gather in a group of their own to stave off  the clickers, who are only interested in then posting the photos on the social media. The chance to meet the players and get to know them is lost.<br>

If only at such events the organisers could announce that there would be 10 to 15 minutes kept aside for photos with the players, it would make life easier for the players who could then actually get to meet the local people and interact with them.<br>

I was at two events in the West Indies where the players were literally manhandled by Indians and I must say the players handled it extremely well, with smiles and courtesy. Virat quite naturally was the most sought after and he is such an engaging young man full of good humour and is courteous to a fault.<br>

The respect he shows to elders, especially former cricketers is very touching.<br>

After the Trinidad Test, he gave his bat to Farokh Engineer and posed for photos with the former ’keeper. Kohli wasn’t even born when Engineer finished playing for India but the respect he showed  was tremendous. What you see on the field and what you get off it are two completely different sides.<br>

On the field he is all charged up looking to do everything he can to win for the team and off the field he is definitely winning hearts and minds of those whom he meets with his down to earth attitude and respectful behaviour. India should be mighty proud of this young man and his bunch of young players, all gentlemen off the field and fierce fighters on it.    <br>

